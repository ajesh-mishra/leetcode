use std::collections::HashMap;

struct Solution {}

impl Solution {
    fn find_gcd(mut x: u32, mut y: u32) -> u32 {
        let mut rem = x % y;
        while rem != 0 {
            x = y;
            y = rem;
            rem = x % y;
        }
        y
    }

    pub fn has_groups_size_x(deck: Vec<i32>) -> bool {
        if deck.len() < 2 {
            return false;
        }
        let mut count_map: HashMap<i32, u32> = HashMap::new();
        for n in deck {
            let c = count_map.entry(n).or_insert(0);
            *c += 1;
        }
        if count_map.len() < 2 {
            return true;
        }
        let mut counts = count_map.values().map(|&x| x).collect::<Vec<u32>>();
        counts.sort_unstable_by(|a, b| a.cmp(b));
        counts.dedup();
        if counts.len() < 2 {
            return true;
        }
        let gcd = Solution::find_gcd(counts[1], counts[0]);
        if gcd < 2 {
            return false;
        }
        count_map.values().all(|&x| x % gcd == 0)
    }
}

