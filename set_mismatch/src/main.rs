use std::collections::HashSet;

struct Solution {}

impl Solution {
    pub fn find_error_nums(nums: Vec<i32>) -> Vec<i32> {
        let len = nums.len();
        let mut running_total = 0;
        let mut unique: HashSet<i32> = HashSet::new();
        let mut repeating = i32::MIN;

        let expected_total = match len % 2 == 0 {
            true => (len / 2) * (len + 1),
            false => len * ((len + 1) / 2)
        };

        for num in nums {
            match unique.insert(num) {
                true => running_total += num,
                false => repeating = num
            }
        }

        vec![repeating, expected_total as i32 - running_total]
    }
}

fn main() {
    let nums = vec![1, 2, 2, 4];
    println!("{:?}", Solution::find_error_nums(nums));
}

